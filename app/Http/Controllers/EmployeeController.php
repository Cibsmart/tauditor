     * @return Response
     */
    public function index()
    {
        $employees = Auth::user()->domain
            ->employees()
            ->paginate()
            ->transform(fn ($employees) => [
                'id' => $employees->id,
                'name' => $employees->name,
                'active' => $employees->active,
            ]);

        // dd($employees);

        return Inertia::render('Employee/Index', [
            'employees' => $employees,
        ]);
    }

